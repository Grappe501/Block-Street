export function t(locale: Locale, key: string, ...replacements: string[]): string {
  let s = TABLES[locale][key] ?? TABLES.en[key] ?? key;
  replacements.forEach((val, i) => {
    s = s.replace(`{${i}}`, val);
  });
  return s;
}

