function classify(p) {
  if (p.startsWith("calendar.admin.")) return "administration";
  if (p.startsWith("calendar.report.")) return "reporting";
  if (p.startsWith("calendar.publication.")) return "publication";
  if (p.startsWith("calendar.conflict.")) return "conflict";
  if (p.startsWith("calendar.staffing.")) return "staffing";
  if (p.startsWith("calendar.candidate.")) return "candidate";
  if (p.startsWith("calendar.approval.")) return "approval";
  if (p.includes("view_")) return "read";
  return "lifecycle";
}

