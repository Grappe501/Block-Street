    role_key,
    display_name: def.display_name,
    geographic_restriction: def.geographic_restriction,
    organizational_restriction: def.scope_types.join(","),
    maps_to_command: def.maps_to_command,
    ...(def.named_holder ? { named_holder: def.named_holder } : {}),
    scope_types: def.scope_types,
